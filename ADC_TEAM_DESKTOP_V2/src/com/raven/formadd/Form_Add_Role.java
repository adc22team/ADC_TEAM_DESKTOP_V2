    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed

        String rol = jTextFieldRole.getText().toString();
        String desc = jTextFieldDesc.getText().toString();
        
        //Creo instancia de la clase RoleCrud
        RoleCrud rc = new RoleCrud();
        
        rc.setRole(rol);
        rc.setDesc(desc);
        
        if (rc.addRole()) {
            JOptionPane.showMessageDialog(this, "S'ha donat d'alta el rol " + rol);
        }
        
        cleanFormAdd();
    }//GEN-LAST:event_jButtonAddActionPerformed

